//转译字符的使用
public class ChangeChar {
	
	public static void main(String args[]) {
		
//		制表位，实现对齐\t
		System.out.println("軽井沢\t御代田");
		
//		换行符\n
		System.out.println("東京\n大阪");
		
//		haha\heihei\\huhu
		System.out.println("haha\\heihei\\\\huhu");
		
//		换行
		System.out.println("ttt\nnnn");
		
	}

}
